        <h1>Política de Privacidad de DevCode</h1>
        <p>Fecha efectiva: 1 de enero, 2023</p>

        <p>Bienvenido a DevCode, un foro para la comunidad de programadores. Valoramos la privacidad de nuestros miembros y visitantes. 
            Esta política de privacidad ("Política") describe cómo recolectamos, usamos, compartimos y protegemos la información personal en relación con los servicios que ofrecemos.
            Esta Política se aplica a todos los usuarios del sitio y está integrada en nuestros Términos de Servicio.</p>
        
        <h2>1. Información que recolectamos</h2>
        <h3>a. Información proporcionada por el usuario</h3>
        <p>Podemos recolectar información personal como tu nombre, dirección de correo electrónico, nombre de usuario y contraseña cuando te registras en DevCode.
             También podemos recopilar cualquier comunicación entre tú y DevCode, así como cualquier información que proporciones en los foros.</p>
        
        <h3>b. Información recolectada automáticamente</h3>
        <p>Cuando accedes a nuestro Sitio, recopilamos cierta información de manera automática, como tu dirección IP, detalles del dispositivo, navegador
             y patrones de navegación mientras usas nuestro Sitio.</p>

        <h3>c. Cookies y tecnologías de seguimiento</h3>
        <p>Usamos cookies y tecnologías similares para rastrear la actividad en nuestro Servicio y mantener cierta información. 
            Las cookies son archivos con una pequeña cantidad de datos que pueden incluir un identificador único anónimo. Las cookies se envían a tu navegador desde un sitio web y 
            se almacenan en tu dispositivo.</p>

        <h2>2. Cómo usamos tu información</h2>
        <p>Usamos la información que recopilamos de varias maneras, incluyendo para:</p>
        <ul>
            <li>Proveer, operar y mantener nuestro Sitio</li>
            <li>Mejorar, personalizar y expandir nuestro Sitio</li>
            <li>Comprender y analizar cómo usas nuestro Sitio</li>
            <li>Desarrollar nuevos productos, servicios, características y funcionalidades</li>
            <li>Comunicarnos contigo, ya sea directamente o a través de uno de nuestros socios, para proporcionarte actualizaciones y 
                otra información relacionada con el sitio web, y para fines de marketing y promoción</li>
            <li>Enviarte correos electrónicos</li>
            <li>Encontrar y prevenir el fraude</li>
        </ul>

        <h2>3. Compartir tu información</h2>
        <p>Podemos compartir la información que hemos recopilado de ti de varias maneras, incluyendo, entre otros:</p>
        <ul>
            <li>Proveedores de servicios</li>
            <li>Transferencias de negocios</li>
            <li>Afiliados</li>
            <li>Asociaciones comerciales</li>
            <li>Publicidad dirigida</li>
            <li>Cumplimiento de la ley</li>
        </ul>

        <h2>4. Tus derechos de privacidad</h2>
        <p>Dependiendo de tu ubicación, puedes tener los siguientes derechos con respecto a tus datos personales:</p>
        <ul>
            <li>El derecho a acceder y recibir una copia de tu información personal</li>
            <li>El derecho a solicitar la corrección de cualquier dato personal incorrecto</li>
            <li>El derecho a solicitar la eliminación de tus datos personales bajo ciertas circunstancias</li>
            <li>El derecho a oponerte al procesamiento de tus datos personales</li>
            <li>El derecho a solicitar la limitación del procesamiento de tus datos personales</li>
            <li>El derecho a la portabilidad de los datos</li>
        </ul>

        <h2>5. Seguridad de la información</h2>
        <p>Nos esforzamos por usar medidas de seguridad razonables diseñadas para proteger contra la pérdida, el mal uso o la alteración de la información personal bajo nuestro control.
             Sin embargo, la seguridad de la información transmitida a través de Internet nunca puede ser garantizada.</p>

        <h2>6. Enlaces a otros sitios</h2>
        <p>Nuestro Sitio puede contener enlaces a sitios web operados por terceros. Si haces clic en un enlace de terceros, serás dirigido al sitio de ese tercero.
             Te recomendamos encarecidamente que revises la Política de Privacidad de cada sitio que visites.</p>

        <h2>7. Cambios a esta política de privacidad</h2>
        <p>Podemos actualizar nuestra Política de Privacidad de vez en cuando. Te notificaremos de cualquier cambio publicando la nueva Política de Privacidad en esta página. 
            Te aconsejamos revisar esta Política de Privacidad periódicamente para cualquier cambio.</p>

        <h2>8. Contacto</h2>
        <p>Si tienes alguna pregunta o duda sobre esta Política, por favor contacta con nosotros en support@devcode.com.</p>
